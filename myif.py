def print_ascii_art(choice):
    if choice == 1:
        # 첫 번째 아스키 아트 (해변)
        art1 = """
            ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
         ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
       ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
        ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
        ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
           ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
        ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
        ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
           ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
         ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
          ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
          ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~ ~
              ~ ~ ~ ~ ~ ~ ~ ~ ~
              🌊🌞 ~~~ 🌴🌊
        """
        print(art1)
    
    elif choice == 2:
        # 두 번째 아스키 아트 (산과 호수)
        art2 = """
             /\      /\   
            /  \    /  \   
           /    \  /    \  
          /______\/______\ 
        /\      /\      /\   
       /  \    /  \    /  \   
      /    \  /    \  /    \  
     /______\/______\/______\ 
     ~~~~~~~~~~🏞️~~~~~~~~~~
        ~~~~~~~~~~🌲~~~~~~~~~
        """
        print(art2)
    
    elif choice == 3:
        # 세 번째 아스키 아트 (하늘과 구름)
        art3 = """
         \     /     
          .-'-. 
       -- (   ) --
          `-.-'    
         /     \
        ~~~~~~~~~~
          ☁️☁️☁️
        """
        print(art3)
    
    else:
        print("잘못된 선택입니다. 1, 2, 3 중에서 선택하세요.")

# 프로그램 시작
while True:
    # 사용자에게 선택 받기
    print("1, 2, 3을 입력하여 아스키 아트를 선택하세요.")
    print("1: 아스키 아트 1")
    print("2: 아스키 아트 2")
    print("3: 아스키 아트 3")
    print("0: 프로그램 종료")

    try:
        choice = int(input("선택: "))
        
        if choice == 0:
            print("프로그램을 종료합니다.")
            break  # 프로그램 종료
        
        # 선택한 아스키 아트를 무한 반복해서 출력
        while True:
            print_ascii_art(choice)
            repeat = input("계속 출력하시겠습니까? (y/n): ").lower()
            if repeat != 'y':
                break  # 더 이상 반복하지 않고 아스키 아트 출력 종료
    except ValueError:
        print("잘못된 입력입니다. 숫자를 입력하세요.")